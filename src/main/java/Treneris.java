public class Treneris {
    private String vardas;
    private String pavarde;

    public Treneris(String vardas, String pavarde) {
        this.vardas = vardas;
        this.pavarde = pavarde;
    }

    public String getVardas() {
        return vardas;
    }

    public String getPavarde() {
        return pavarde;
    }

    public Treneris(){};
}
